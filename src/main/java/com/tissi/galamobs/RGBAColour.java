package com.tissi.galamobs;

public class RGBAColour {
    public final float r;
    public final float g;
    public final float b;
    public final float a;

    public RGBAColour(float r, float g, float b, float a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }
}
